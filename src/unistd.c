#include "unistd.h"
#include <stddef.h>

#define GET_SYS_RETVAL(var) __asm__ volatile ("" : "=a"(var))

/* ------------------------------------------------------------------------------------------------- */
int write(const char *file, const void *buf, size_t n)
{
        __asm__ volatile (
                "int $0x80"
                ::
                "a"(0), "b"(file), "c"(buf), "d"(n)
        );

        int ret = 0;
        GET_SYS_RETVAL(ret);
        return ret;
}
/* ------------------------------------------------------------------------------------------------- */

/* ------------------------------------------------------------------------------------------------- */
int read(const char *file, void *buf, size_t n)
{
        __asm__ volatile (
                "int $0x80"
                ::
                "a"(1), "b"(file), "c"(buf), "d"(n)
        );
        
        int ret = 0;
        GET_SYS_RETVAL(ret);
        return ret;
}
/* ------------------------------------------------------------------------------------------------- */

/* ------------------------------------------------------------------------------------------------- */
int exec(const char *file)
{
        __asm__ volatile (
                "int $0x80"
                ::
                "a"(2), "b"(file)
        );

        int ret = 0;
        GET_SYS_RETVAL(ret);
        return ret;
}
/* ------------------------------------------------------------------------------------------------- */